#formato de cadenas

print ("%s, %s, %.2f, %f" % (12,"spam", 1/3, 1/3.0))

print ("{0}, {1}, {2:.2f}, {3:f}".format (12,"spam", 1/3, 1/3.0))

print ("%c" % (64)) #char
print ("%s" % ("String")) #string
print ("%o" % (15)) #octal
print ("%x" % (15)) #hexadecimal
print ("%.2f" % (15)) #float
print ("%i" % (15.2)) #integer
print ("%e" % (15.2)) #exponente